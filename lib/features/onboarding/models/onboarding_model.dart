import 'dart:ui';

class OnboardingModel {
  final String title;
  final String description;
  final String image;
  final Color color;
  final Color textColor;
  final String onboardingCounter;
  final double height;

  OnboardingModel({
    required this.title,
    required this.description,
    required this.image,
    required this.color,
    required this.textColor,
    required this.onboardingCounter,
    required this.height,
  });

}
